    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        DefaultTableModel model = (DefaultTableModel) saverTable.getModel();
        Money[] moneys = new Money[MoneySaver.moneys.size()];

        for (int i = 0; i < MoneySaver.moneys.size(); i++) {
            moneys[i] = MoneySaver.moneys.get(i);
            model.addRow(new Object[]{moneys[i].getName(), moneys[i].getIncome(), moneys[i].getOutcome(), moneys[i].getDate(), moneys[i].getSaving()});
        }
    }//GEN-LAST:event_formWindowOpened

